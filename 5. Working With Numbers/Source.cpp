#include <iostream>

using namespace std;

int main()
{
	  ///////////////////////////////////////
	 //       Working With Numbers        //
	///////////////////////////////////////

	cout << 40 << endl;			  // Will print 40
	cout << -40 << endl;		  // Will print -40
	cout << 1 + 2 << endl;		  // 3
	cout << 2 - 1 << endl;		  // 1
	cout << 2 * 1 << endl;		  // 2
	cout << 2 / 1 << endl;		  // 2
	cout << 10 % 3 << endl;		  // 1 = 10/3 = 3+1/3
	cout << (1 + 2) * 10 << endl; // 30

	int wnum = 10;
	wnum++;
	cout << wnum << endl;

	return 0;
}